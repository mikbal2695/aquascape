-- 6. Storage Bucket & Policies for Article Images Upload
-- =======================================================

-- Create storage bucket
insert into storage.buckets (id, name, public) 
values ('article-images', 'article-images', true)
on conflict (id) do nothing;

-- Allow public read access to images
drop policy if exists "Allow public read access" on storage.objects;
create policy "Allow public read access" on storage.objects 
  for select using (bucket_id = 'article-images');

-- Allow authenticated/service role to upload images
drop policy if exists "Allow service role upload" on storage.objects;
create policy "Allow service role upload" on storage.objects 
  for insert with check (bucket_id = 'article-images');

