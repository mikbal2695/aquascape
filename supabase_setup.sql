-- 1. Create posts table
create table if not exists posts (
  id uuid default gen_random_uuid() primary key,
  slug text unique not null,
  title text not null,
  description text not null,
  content text not null,
  image text not null default '/paludarium.png',
  author text not null default 'Aquascaper',
  tags text[] not null default '{}',
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- 2. Create page_views table for analytics
create table if not exists page_views (
  id bigint generated by default as identity primary key,
  slug text not null,
  ip_hash text not null,
  user_agent text,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- 3. Enable Row Level Security (RLS)
alter table posts enable row level security;
alter table page_views enable row level security;

-- 4. Create RLS Policies
-- Allow anyone to read posts
create policy "Allow public read posts" on posts
  for select using (true);

-- Allow server-side service role to insert/modify posts
create policy "Allow service role all on posts" on posts
  using (true)
  with check (true);

-- Allow anyone to insert page views (for analytics)
create policy "Allow public insert page_views" on page_views
  for insert with check (true);

-- Allow server-side service role to read/delete page views
create policy "Allow read page_views" on page_views
  for select using (true);

